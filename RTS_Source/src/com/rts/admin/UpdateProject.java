    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jTextField2.getText().isEmpty())
        {
           if(JOptionPane.showConfirmDialog(this, "Do you want to confirm the updation of this Project?", "Update Project", JOptionPane.YES_NO_OPTION) == 0)
            {
             String projectName = (String)jComboBox2.getSelectedItem();
             String updatedName = jTextField2.getText();
             System.out.println("Updated name is: "+updatedName);
             int projectId=0;
             for (int i=0;i<projectsList.size();i++)
             {
                 projects = (Projects) projectsList.get(i);
                // model.addElement(projects.getProjectOwner())  ;
                 if(projectName.equals(projects.getProjectName()))
                 {
                      jLabel2.setEnabled(true);
                      //jList1.setEnabled(true);
                     jLabel1.setEnabled(true);
                     projectId= projects.getProjectId();



                    projectToPass.setProjectId(projectId);
                     projectToPass.setProjectName(updatedName);
                     projectToPass.setProjectOwner(projects.getProjectOwner());
                 }

            }
             projectsDAO.modifyProjectToDb(projectToPass);
            }
            else
            {
               jTextField2.setText("");
            }
        }
        else
        {
            jTextField2.setText("Please set the correct Project Name here!!");
            
        }
        jComboBox1ActionPerformed(evt);
    }//GEN-LAST:event_jButton1ActionPerformed

